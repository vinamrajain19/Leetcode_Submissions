class Solution{
    public:
    long long findMinDiff(vector<long long> a, long long n, long long m){
        sort(a.begin(),a.end());
        int s = 0;
        int e = m-1;
        
        long long ans = 1e9;
        while(e < n){
            long long diff = a[e] - a[s];
            ans = min(ans, diff);
            e++;
            s++;
        }
        
        return ans;
    }   
};

