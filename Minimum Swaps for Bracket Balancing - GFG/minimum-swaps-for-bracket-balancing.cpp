class Solution{   
public:
    int minimumNumberOfSwaps(string S){
        // code here 
        int open = 0;
        int close = 0;
        int ans = 0;
        for(auto it : S){
            if(it == ']') close++;
            else{
                if(close > open) ans += close -open;
                open++;
            }
                    }
        return ans;
    }
};

